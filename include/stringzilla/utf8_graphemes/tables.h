/**
 *  @brief Gather-free UAX-29 Grapheme_Cluster_Break property tables. Each codepoint resolves to a packed
 *  descriptor byte `gcb | incb << 4 | extended_pictographic << 6` (Grapheme_Cluster_Break in bits 0-3,
 *  Indic_Conjunct_Break in bits 4-5, Extended_Pictographic in bit 6), consumed by GB9c / GB11. Derived by:
 *
 *  @code{.py}
 *  # GraphemeBreakProperty.txt -> gcb in {Other,CR,LF,Control,Extend,ZWJ,RI,Prepend,SpacingMark,L,V,T,LV,LVT}
 *  # DerivedCoreProperties.txt  -> InCB in {None,Consonant,Extend,Linker}
 *  # emoji-data.txt             -> Extended_Pictographic flag
 *  # The 18 distinct descriptor bytes are deduplicated into id_to_desc[18]; tables store the *index*.
 *  # Hangul syllables U+AC00..U+D7A3 are the LV/LVT formula (no table), excluded at generation time.
 *  # BMP: stage_hi[cp >> 8] -> stage_mid[mid][(cp >> 4) & 0xF] -> stage_sub[sub][cp & 0xF] -> id_to_desc.
 *  # astral (cp >= 0x10000): a sorted (lo, hi, descriptor-index) range list.
 *  @endcode
 */

