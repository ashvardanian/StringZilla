/**
 *  @brief Gather-free UAX-29 Word_Break property tables. A codepoint maps to one of the 16
 *  `sz_utf8_word_break_t` values (see the walk in `sz_rune_word_break_property`). Derived from the UCD by:
 *
 *  @code{.py}
 *  import urllib.request, re
 *  PROPS = {"CR": 1, "LF": 2, "Newline": 3, "Extend": 4, "ZWJ": 5, "Format": 6, "Regional_Indicator": 7,
 *           "ALetter": 8, "Hebrew_Letter": 9, "Numeric": 10, "Katakana": 11, "ExtendNumLet": 12,
 *           "MidLetter": 13, "MidNum": 14, "MidNumLet": 15, "Single_Quote": 15, "Double_Quote": 15}
 *  url = "https://www.unicode.org/Public/17.0.0/ucd/auxiliary/WordBreakProperty.txt"
 *  properties = {}
 *  for line in urllib.request.urlopen(url).read().decode().splitlines():
 *      match = re.match(r"([0-9A-F]+)(?:\.\.([0-9A-F]+))?\s*;\s*(\w+)", line)
 *      if match and match.group(3) in PROPS:
 *          low, high = int(match.group(1), 16), int(match.group(2) or match.group(1), 16)
 *          for codepoint in range(low, high + 1): properties[codepoint] = PROPS[match.group(3)]
 *
 *  # The map above is the source of truth; the serial oracle and the Ice Lake kernel compress it differently.
 *  # big ranges: a few huge homogeneous blocks (Latin-Ext, CJK-Ext-A, CJK-Unified, Hangul) become arithmetic
 *  #   (low, high, class) triples the serial walk scans first, so they never enter the LUTs.
 *  # flat_lut_0800: one property byte per codepoint for codepoint < 0x800 (every 1- and 2-byte UTF-8).
 *  # trie: the scattered 3-byte BMP residue in a B=8 / SB=16 two-level trie of leaf bytes, shared by both
 *  #   (trie_l1[codepoint_block >> 4] → trie_l2 row → trie_leaf[leaf * 8 + (codepoint & 7)]).
 *  # astral (codepoint >= 0x10000): the serial oracle linearly scans the sorted (low, high, class) ranges;
 *  #   the Ice Lake kernel walks a register-resident 4-stage trie over offset = codepoint - 0x10000 (an
 *  #   8/4/4/4 split), built bottom-up with per-level de-duplication so equal blocks share an index:
 *  def dedup(blocks):
 *      unique, ids, table = {}, [], []
 *      for block in map(tuple, blocks):
 *          if block not in unique: unique[block] = len(table); table.append(block)
 *          ids.append(unique[block])
 *      return ids, table
 *  word_class = lambda codepoint: properties.get(codepoint, 0)
 *  leaf_ids, leaf = dedup([word_class(0x10000 + high * 16 + low) for low in range(16)] for high in range(0x10000))
 *  stage2_ids, stage2 = dedup(leaf_ids[i * 16:i * 16 + 16] for i in range(0x1000))  # group by (offset >> 4) & 0xF
 *  stage1_ids, stage1 = dedup(stage2_ids[i * 16:i * 16 + 16] for i in range(0x100))  # group by (offset >> 8) & 0xF
 *  astral_s0 = stage1_ids  # one stage-1 block id per (offset >> 12)
 *  astral_s1 = [value for row in stage1 for value in row]
 *  astral_s2 = [value for row in stage2 for value in row]
 *  astral_leaf = [value for row in leaf for value in row]  # word-break class indexed by (offset & 0xF)
 *  @endcode
 */

